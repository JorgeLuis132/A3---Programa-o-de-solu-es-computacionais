    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String compromisso, responsavel, cliente, local;
            
            compromisso = txtcompromisso.getText();
            responsavel = txtresponsavel.getText();
            cliente = txtcliente.getText();
            local = txtlocal.getText();
            
            AgendamentoDTO objAgendamentoDTO = new AgendamentoDTO();
            objAgendamentoDTO.setCompromisso(compromisso);
            objAgendamentoDTO.setResponsavel(responsavel);
            objAgendamentoDTO.setCliente(cliente);
            objAgendamentoDTO.setLocal(local);
            
            AgendamentoDAO objAgendamentoDAO = new AgendamentoDAO();
            objAgendamentoDAO.CadastrarAgenda(objAgendamentoDTO);
            JOptionPane.showMessageDialog(null, "Agenda cadastrada no banco de dados");
            listarAgenda();
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

