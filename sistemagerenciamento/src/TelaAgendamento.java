      private void listarAgenda() {
          try {
              AgendamentoDAO objAgendamentoDAO = new AgendamentoDAO();
               DefaultTableModel model = (DefaultTableModel) tabelaagenda.getModel();
            model.setNumRows(0);
            ArrayList<AgendamentoDTO> lista = objAgendamentoDAO.PesquisarAgenda();
            
             for (int i = 0; i < lista.size(); i++) {
                model.addRow(new Object[] {
                lista.get(i).getCodigo(),
                lista.get(i).getCompromisso(),
                lista.get(i).getResponsavel(),
                lista.get(i).getCliente(),
                lista.get(i).getLocal()
             });
            }
          } catch (Exception e) {
               JOptionPane.showMessageDialog(null, "listar valores" + e);
          }
      }
      private void CarregarCampos() {
          int setar = tabelaagenda.getSelectedRow();
          txtcodigo.setText(tabelaagenda.getModel().getValueAt(setar, 0).toString());
          txtcompromisso.setText(tabelaagenda.getModel().getValueAt(setar, 1).toString());
          txtresponsavel.setText(tabelaagenda.getModel().getValueAt(setar, 2).toString());
          txtcliente.setText(tabelaagenda.getModel().getValueAt(setar, 3).toString());
          txtlocal.setText(tabelaagenda.getModel().getValueAt(setar, 3).toString());
      }
      private void limparDescricao() {
          txtdescricao.setText("");
      }
      
      private void excluiirAgenda() {
          int codigo = 0;
          codigo = Integer.parseInt(txtcodigo.getText());
          AgendamentoDTO objAgendamentoDTO = new AgendamentoDTO();
          objAgendamentoDTO.setCodigo(codigo);
          AgendamentoDAO objAgendamentoDAO = new AgendamentoDAO();
          objAgendamentoDAO.ExcluirAgenda(objAgendamentoDTO);
      }

