 private void login(){
       try {
            String emailadm = txtemailadm.getText();
            String senhaadm = txtsenhaadm.getText();

            AdmDTO objadmdto = new AdmDTO();
            objadmdto.setEmail_adm(emailadm);
            objadmdto.setSenha_adm(senhaadm);
            
            admDAO objadmdao = new admDAO();
            ResultSet rsadmdao = objadmdao.autenticacaoadm(objadmdto);
            
            if (rsadmdao.next()) {
                JOptionPane.showMessageDialog(null, "Administrador logado com sucesso");
                TelaInicialAdm tiadm = new TelaInicialAdm();
                tiadm.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Email ou senha inv√°lido");
            }
            
        } catch (SQLException erro) {
              JOptionPane.showMessageDialog(null, "Loginadmview" + erro);
             
        }

 }
